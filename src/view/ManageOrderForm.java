    private void loadAllOders() {
        try {
            ArrayList<OrderDTO> o = new ManageOrderController().getAllOders();
            DefaultTableModel dtm = (DefaultTableModel) tblOders.getModel();
            dtm.setRowCount(0);

            for (OrderDTO od : o) {
                Object[] row = {od.getoId(), od.getoDate(), od.getCusId()};
                dtm.addRow(row);
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
