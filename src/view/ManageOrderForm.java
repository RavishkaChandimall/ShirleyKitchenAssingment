    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            DefaultTableModel dtm = (DefaultTableModel) tblOders.getModel();
            String oId = dtm.getValueAt(tblOders.getSelectedRow(), 0).toString();

            boolean cancel = new ManageOrderController().cancelOrder(oId);
            if (cancel) {
                JOptionPane.showMessageDialog(rootPane, "Canceled!");
                loadAllOders();
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ManageOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ManageOrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

