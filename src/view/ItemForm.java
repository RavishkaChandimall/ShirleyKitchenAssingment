    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String desc = txtItemDesc.getText();
            String number = txtItemCode.getText();
            int qty = Integer.parseInt(txtItemQty.getText());
            double price = Double.parseDouble(txtItemPrice.getText());
            
            boolean addCustomer = new ItemController().addItem(new ItemDTO(number, desc, qty, price));
            if (addCustomer) {
                JOptionPane.showMessageDialog(rootPane, "Added!");
                loadAllItems();
            } else {
                JOptionPane.showMessageDialog(rootPane, "not Added!");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            String code = txtItemCode.getText();
            
            ItemDTO itm = new ItemController().searchItem(code);
            if (itm != null) {
                txtItemCode.setText(itm.getItemCode());
                txtItemDesc.setText(itm.getDesc());
                txtItemQty.setText(String.valueOf(itm.getQty()));
                txtItemPrice.setText(String.valueOf(itm.getPrice()));
            } else {
                JOptionPane.showConfirmDialog(rootPane, "Not found!");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

