    private void loadAllItems() {
        try {
            DefaultTableModel model = (DefaultTableModel) tblItem.getModel();
            model.setRowCount(0);

            ArrayList<ItemDTO> allItem = new ItemController().getAllItem();
            for (ItemDTO itm : allItem) {
                Object[] row = {itm.getItemCode(),itm.getDesc(),itm.getQty(),itm.getPrice()};
                model.addRow(row);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ItemForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
