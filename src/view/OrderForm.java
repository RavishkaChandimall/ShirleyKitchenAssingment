    private void loadAllItems() {
        try {
            ArrayList<ItemDTO> loadAllItems = new OrderController().loadAllItems();
            for (ItemDTO itm : loadAllItems) {
                cmbItem.addItem(itm.getItemCode());
            }
        } catch (SQLException ex) {
            Logger.getLogger(OrderForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(OrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void setDate() {
        String date = LocalDate.now().toString();
        txtODate.setText(date);
    }

    private int checkItem(String itmCode) {
        DefaultTableModel dtm = (DefaultTableModel) tblOrders.getModel();
        for (int i = 0; i < dtm.getRowCount(); i++) {
            String t = tblOrders.getValueAt(i, 0).toString();
            if (t.equalsIgnoreCase(itmCode)) {
                return i;
            }
        }
        return -1;
    }

    private void getGrandTotal() {
        double total = 0;
        for (int i = 0; i < tblOrders.getRowCount(); i++) {
            total += Double.parseDouble(tblOrders.getValueAt(i, 4).toString());
        }

        lblGrandTotal.setText(String.valueOf(total));
    }
