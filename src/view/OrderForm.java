    private void btnAddTableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTableActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) tblOrders.getModel();
        String itmCode = txtItemCode.getText();
        String itmName = txtItemName.getText();
        int qty = Integer.parseInt(txtBuyQty.getText());
        double price = Double.parseDouble(txtUnitPrice.getText());
        double total = price * qty;

        int currentRow = checkItem(itmCode);
        if (currentRow == -1) {
            Object[] row = {itmCode, itmName, qty, price, total};
            dtm.addRow(row);
        } else {
            qty += Integer.parseInt(dtm.getValueAt(currentRow, 2).toString());
            total = price * qty;
            tblOrders.setValueAt(qty, currentRow, 2);
            tblOrders.setValueAt(total, currentRow, 4);
        }

        getGrandTotal();

    }//GEN-LAST:event_btnAddTableActionPerformed

    private void btnPurhchaseOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPurhchaseOrderActionPerformed
        try {
            String oId = txtOID.getText();
            String oDate = txtODate.getText();
            String cusId = txtSearcCusNo.getText();
            double fullTotal = Double.parseDouble(lblGrandTotal.getText());

            OrderDTO o = new OrderDTO(oId, oDate, cusId, fullTotal);

            ArrayList<OrderDetailDTO> odList = new ArrayList<>();
            for (int i = 0; i < dtm.getRowCount(); i++) {
                String itmCode = dtm.getValueAt(i, 0).toString();
                int qty = Integer.parseInt(dtm.getValueAt(i, 2).toString());
                double price = Double.parseDouble(dtm.getValueAt(i, 3).toString());

                OrderDetailDTO od = new OrderDetailDTO(oId, itmCode, qty, price);
                odList.add(od);
            }

            boolean added = new OrderController().placeOrder(o);
            if (added) {
                JOptionPane.showMessageDialog(rootPane, "Placed!");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Not Placed!");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(OrderForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPurhchaseOrderActionPerformed

